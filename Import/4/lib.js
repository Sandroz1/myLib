 

export function masSum(mas){
    let Sum = 0

    for(let i = 0; i < mas.length ; i++){
     Sum = Sum + mas[i]
    }
    return Sum
}
// export default masSum



export function masMultiplication(mas){
    let Sum = 1

    for(let i = 0; i < mas.length ; i++){
     Sum = Sum * mas[i]
    }
    return Sum
}
// export default masMultiplication

